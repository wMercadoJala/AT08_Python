def add(num1, num2):
    print(int(num1) + int(num2))


def subtract(num1, num2):
    print(int(num1) - int(num2))


def multiplication(num1, num2):
    print(int(num1) * int(num2))


def division(num1, num2):
    print(int(num1) / int(num2))


def mod(num1, num2):
    print(int(num1) % int(num2))


def exp(num1, num2):
    print(int(num1) ** int(num2))


def exp2(num1, num2):
    print(float(num1) // float(num2))


